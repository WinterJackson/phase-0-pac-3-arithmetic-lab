function add(x,y){
    let result =  x + y;
    return result
}
function subtract(x,y){
    let result = x - y;
    return result
}
function multiply(x,y){
    let result = x * y
    return result
}
function divide(x,y){
    let result = x / y
    return result
}
function increment(n){
    return n + 1;
}
function decrement(n){
    return n -1;
}
function makeInt(n){
    return  parseInt(n, 10);
 }
 function preserveDecimal(n){
     return parseFloat(n)
 }